# Tipos de Primitivos de dados do Python
# INT (Inteiro) - 7, 4, 103509, 45, 69
# FLOAT (Flutuante) - 7.0, 493.04009, 69.6969
# BOOL (Booleano) - True (verdadeiro) False (falso)
# STR (String) - 'Ola mundo!', '', 'Batata eh bom'

primeiro_numero = int(input('Primeiro numero: '))
segundo_numero = int(input('Segundo numero: '))
soma = primeiro_numero + segundo_numero

print(f'A soma entre {primeiro_numero} e {segundo_numero} vale {soma}')
print(type(primeiro_numero)) # Type() serve pra ver o tipo do valor inserido