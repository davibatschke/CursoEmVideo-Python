# Ordem de Precedencia!
# 1. Parenteses
# 2. Potencias
# 3. Vezes, Dividido, Divisao Inteira, Resto da Divisao
# 4. Adicao, Subtracao

print(5 + 2) # 7
print(5 - 2) # 3
print(5 * 2) # 10
print(5 / 2) # 2.5
print(5 ** 2) # 25
print(5 // 2) # 2
print(5 % 2) # 1

# Outras coisas que podemos fazer com o Print()
nome = 'Davi'
print(f'Oii {nome:>20} tudo bom?')
print(f'Oii {nome:<20} tudo bom?')
print(f'Oii {nome:^20} tudo bom?')
print(f'Oii {nome:-^20} tudo bom?')